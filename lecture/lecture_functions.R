

#make add-one function ####
add_one <- function(x){
  ret_value <- x+1
  return (ret_value)
}

add_one(3)

#make square root function ####

square_root <- function(x){
  ret_value <- sqrt(x)
  return(ret_value)
}

square_root(16)

#make max vs min function ####

max_minus_min <- function(x){
  ret_value <- (max(x)-min(x))
  return(ret_value)
}

max_minus_min(c(4,7,1,6,8))

